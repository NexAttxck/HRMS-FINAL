-- ============================================================
-- 1. DEPARTMENTS
-- ============================================================
CREATE TABLE DEPARTMENTS (
  DEPARTMENT_ID   NUMBER(10)    GENERATED ALWAYS AS IDENTITY,
  DEPT_NAME       VARCHAR2(100) NOT NULL,
  DESCRIPTION     CLOB,
  LOCATION        VARCHAR2(150),
  BUDGET          NUMBER(15,2)  DEFAULT 0,
  CREATED_AT      NUMBER(12)    DEFAULT 0 NOT NULL,
  CONSTRAINT DEPARTMENTS_PK        PRIMARY KEY (DEPARTMENT_ID),
  CONSTRAINT DEPARTMENTS_NAME_UQ   UNIQUE      (DEPT_NAME)
);

-- ============================================================
-- 2. USER_ACCOUNTS
-- ============================================================
CREATE TABLE USER_ACCOUNTS (
  USER_ID         NUMBER(10)    GENERATED ALWAYS AS IDENTITY,
  USERNAME        VARCHAR2(80)  NOT NULL,
  EMAIL           VARCHAR2(150) NOT NULL,
  PASSWORD        VARCHAR2(255) NOT NULL,
  ROLE            VARCHAR2(20)  DEFAULT 'Employee' NOT NULL,
  STATUS          VARCHAR2(10)  DEFAULT 'Active'   NOT NULL,
  DEPARTMENT_ID   NUMBER(10),
  CREATED_AT      NUMBER(12)    DEFAULT 0 NOT NULL,
  CONSTRAINT USER_ACCOUNTS_PK          PRIMARY KEY (USER_ID),
  CONSTRAINT USER_ACCOUNTS_EMAIL_UQ    UNIQUE      (EMAIL),
  CONSTRAINT USER_ACCOUNTS_UNAME_UQ    UNIQUE      (USERNAME),
  CONSTRAINT USER_ACCOUNTS_ROLE_CHK    CHECK       (ROLE   IN ('Super Admin','Manager','Employee')),
  CONSTRAINT USER_ACCOUNTS_STATUS_CHK  CHECK       (STATUS IN ('Active','Inactive')),
  CONSTRAINT USER_ACCOUNTS_DEPT_FK     FOREIGN KEY (DEPARTMENT_ID) REFERENCES DEPARTMENTS(DEPARTMENT_ID) ON DELETE SET NULL
);

-- ============================================================
-- 3. POSITIONS
-- ============================================================
CREATE TABLE POSITIONS (
  POSITION_ID     NUMBER(10)    GENERATED ALWAYS AS IDENTITY,
  TITLE           VARCHAR2(100) NOT NULL,
  DEPARTMENT_ID   NUMBER(10),
  SALARY_MIN      NUMBER(12,2)  DEFAULT 0,
  SALARY_MAX      NUMBER(12,2)  DEFAULT 0,
  CREATED_AT      NUMBER(12)    DEFAULT 0 NOT NULL,
  CONSTRAINT POSITIONS_PK       PRIMARY KEY (POSITION_ID),
  CONSTRAINT POSITIONS_DEPT_FK  FOREIGN KEY (DEPARTMENT_ID) REFERENCES DEPARTMENTS(DEPARTMENT_ID) ON DELETE SET NULL
);

-- ============================================================
-- 4. EMPLOYEES
-- ============================================================
CREATE TABLE EMPLOYEES (
  EMPLOYEE_ID                    NUMBER(10)    GENERATED ALWAYS AS IDENTITY,
  USER_ID                        NUMBER(10),
  EMPLOYEE_NO                    VARCHAR2(30),
  FIRST_NAME                     VARCHAR2(80)  NOT NULL,
  MIDDLE_NAME                    VARCHAR2(80),
  LAST_NAME                      VARCHAR2(80)  NOT NULL,
  SUFFIX                         VARCHAR2(10),
  GENDER                         VARCHAR2(20),
  EMAIL                          VARCHAR2(150),
  PHONE                          VARCHAR2(30),
  DEPARTMENT_ID                  NUMBER(10),
  POSITION_ID                    NUMBER(10),
  JOB_TITLE                      VARCHAR2(100),
  EMPLOYMENT_TYPE                VARCHAR2(20)  DEFAULT 'Full-Time' NOT NULL,
  STATUS                         VARCHAR2(20)  DEFAULT 'Probationary' NOT NULL,
  HIRE_DATE                      DATE,
  BASIC_SALARY                   NUMBER(12,2)  DEFAULT 0 NOT NULL,
  CIVIL_STATUS                   VARCHAR2(15),
  DATE_OF_BIRTH                  DATE,
  PLACE_OF_BIRTH                 VARCHAR2(150),
  NATIONALITY                    VARCHAR2(60)  DEFAULT 'Filipino',
  BLOOD_TYPE                     VARCHAR2(5),
  HIGHEST_EDUCATION              VARCHAR2(80),
  ADDRESS                        CLOB,
  SSS_NO                         VARCHAR2(30),
  PHILHEALTH_NO                  VARCHAR2(30),
  PAGIBIG_NO                     VARCHAR2(30),
  TIN_NO                         VARCHAR2(30),
