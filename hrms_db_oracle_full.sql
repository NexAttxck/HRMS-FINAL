  JOB_TYPE        VARCHAR2(40),
  STATUS          VARCHAR2(10)  DEFAULT 'Open' NOT NULL,
  POSTED_BY       NUMBER(10),
  CREATED_AT      NUMBER(12)    DEFAULT 0 NOT NULL,
  UPDATED_AT      NUMBER(12)    DEFAULT 0 NOT NULL,
  CONSTRAINT JOB_POSTINGS_PK          PRIMARY KEY (JOB_ID),
  CONSTRAINT JOB_POSTINGS_STATUS_CHK  CHECK       (STATUS IN ('Open','Closed','On Hold')),
  CONSTRAINT JOB_POSTINGS_DEPT_FK     FOREIGN KEY (DEPARTMENT_ID) REFERENCES DEPARTMENTS(DEPARTMENT_ID) ON DELETE SET NULL,
  CONSTRAINT JOB_POSTINGS_USER_FK     FOREIGN KEY (POSTED_BY)     REFERENCES USER_ACCOUNTS(USER_ID)     ON DELETE SET NULL
);

