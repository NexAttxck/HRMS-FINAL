  CONSTRAINT SYSTEM_NOTIFICATIONS_PK        PRIMARY KEY (NOTIFICATION_ID),
  CONSTRAINT SYSTEM_NOTIFICATIONS_TYPE_CHK  CHECK       (NOTIF_TYPE IN ('info','success','warning','danger')),
  CONSTRAINT SYSTEM_NOTIFICATIONS_USER_FK   FOREIGN KEY (USER_ID) REFERENCES USER_ACCOUNTS(USER_ID) ON DELETE CASCADE
);

