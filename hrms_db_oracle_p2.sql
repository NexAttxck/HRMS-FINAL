-- ============================================================
-- 23. LMS_ENROLLMENTS
-- ============================================================
CREATE TABLE LMS_ENROLLMENTS (
  ENROLLMENT_ID   NUMBER(10)   GENERATED ALWAYS AS IDENTITY,
