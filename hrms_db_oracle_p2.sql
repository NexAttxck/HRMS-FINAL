-- 15. BENEFITS
-- ============================================================
CREATE TABLE BENEFITS (
  BENEFIT_ID    NUMBER(10)    GENERATED ALWAYS AS IDENTITY,
  BENEFIT_NAME  VARCHAR2(100) NOT NULL,
  DESCRIPTION   CLOB,
  BENEFIT_TYPE  VARCHAR2(50)  DEFAULT 'Other' NOT NULL,
  VALUE         NUMBER(12,2)  DEFAULT 0 NOT NULL,
  CREATED_AT    NUMBER(12)    DEFAULT 0 NOT NULL,
  CONSTRAINT BENEFITS_PK           PRIMARY KEY (BENEFIT_ID),
  CONSTRAINT BENEFITS_NAME_UQ      UNIQUE      (BENEFIT_NAME),
  CONSTRAINT BENEFITS_TYPE_CHK     CHECK       (BENEFIT_TYPE IN ('Health','Government','Bonus','Allowance','Other'))
);

-- ============================================================
-- 16. EMPLOYEE_BENEFITS
-- ============================================================
CREATE TABLE EMPLOYEE_BENEFITS (
  EMP_BENEFIT_ID  NUMBER(10) GENERATED ALWAYS AS IDENTITY,
  EMPLOYEE_ID     NUMBER(10) NOT NULL,
  BENEFIT_ID      NUMBER(10) NOT NULL,
  ENROLLED_AT     DATE,
  CONSTRAINT EMPLOYEE_BENEFITS_PK      PRIMARY KEY (EMP_BENEFIT_ID),
  CONSTRAINT EMPLOYEE_BENEFITS_UQ      UNIQUE      (EMPLOYEE_ID, BENEFIT_ID),
  CONSTRAINT EMPLOYEE_BENEFITS_EMP_FK  FOREIGN KEY (EMPLOYEE_ID) REFERENCES EMPLOYEES(EMPLOYEE_ID) ON DELETE CASCADE,
  CONSTRAINT EMPLOYEE_BENEFITS_BEN_FK  FOREIGN KEY (BENEFIT_ID)  REFERENCES BENEFITS(BENEFIT_ID)   ON DELETE CASCADE
);

-- ============================================================
-- 17. ONBOARDING_TASKS
-- ============================================================
CREATE TABLE ONBOARDING_TASKS (
  TASK_ID       NUMBER(10)    GENERATED ALWAYS AS IDENTITY,
  EMPLOYEE_ID   NUMBER(10)    NOT NULL,
  TASK_NAME     VARCHAR2(200) NOT NULL,
  STAGE         VARCHAR2(20)  DEFAULT 'Pre-boarding' NOT NULL,
  STATUS        VARCHAR2(20)  DEFAULT 'Not Started'  NOT NULL,
  DUE_DATE      DATE,
  NOTES         CLOB,
  CREATED_AT    NUMBER(12)    DEFAULT 0 NOT NULL,
  UPDATED_AT    NUMBER(12)    DEFAULT 0 NOT NULL,
  CONSTRAINT ONBOARDING_TASKS_PK          PRIMARY KEY (TASK_ID),
  CONSTRAINT ONBOARDING_TASKS_STAGE_CHK   CHECK       (STAGE  IN ('Pre-boarding','Week 1','Week 2','Integration')),
  CONSTRAINT ONBOARDING_TASKS_STATUS_CHK  CHECK       (STATUS IN ('Not Started','In Progress','Completed','On Hold')),
  CONSTRAINT ONBOARDING_TASKS_EMP_FK      FOREIGN KEY (EMPLOYEE_ID) REFERENCES EMPLOYEES(EMPLOYEE_ID) ON DELETE CASCADE
);

-- ============================================================
-- 18. EMPLOYEE_DOCUMENTS
-- ============================================================
CREATE TABLE EMPLOYEE_DOCUMENTS (
  DOCUMENT_ID   NUMBER(10)    GENERATED ALWAYS AS IDENTITY,
  EMPLOYEE_ID   NUMBER(10)    NOT NULL,
  DOCUMENT_TYPE VARCHAR2(100) NOT NULL,
  FILE_NAME     VARCHAR2(255) NOT NULL,
  FILE_PATH     VARCHAR2(500) NOT NULL,
  UPLOADED_AT   NUMBER(12)    DEFAULT 0 NOT NULL,
  CONSTRAINT EMPLOYEE_DOCUMENTS_PK      PRIMARY KEY (DOCUMENT_ID),
  CONSTRAINT EMPLOYEE_DOCUMENTS_EMP_FK  FOREIGN KEY (EMPLOYEE_ID) REFERENCES EMPLOYEES(EMPLOYEE_ID) ON DELETE CASCADE
);

-- ============================================================
-- 19. EMPLOYEE_DOC_CHECKLISTS
-- ============================================================
CREATE TABLE EMPLOYEE_DOC_CHECKLISTS (
  CHECKLIST_ID  NUMBER(10)    GENERATED ALWAYS AS IDENTITY,
  EMPLOYEE_ID   NUMBER(10)    NOT NULL,
  DOCUMENT_TYPE VARCHAR2(100) NOT NULL,
  STATUS        VARCHAR2(15)  DEFAULT 'Pending' NOT NULL,
  DOCUMENT_ID   NUMBER(10),
  NOTES         VARCHAR2(255),
  CREATED_AT    NUMBER(12)    DEFAULT 0 NOT NULL,
  UPDATED_AT    NUMBER(12)    DEFAULT 0 NOT NULL,
  CONSTRAINT EMP_DOC_CHECKLISTS_PK         PRIMARY KEY (CHECKLIST_ID),
  CONSTRAINT EMP_DOC_CHECKLISTS_STATUS_CHK CHECK       (STATUS IN ('Pending','Submitted','Approved','Waived')),
  CONSTRAINT EMP_DOC_CHECKLISTS_EMP_FK     FOREIGN KEY (EMPLOYEE_ID) REFERENCES EMPLOYEES(EMPLOYEE_ID)           ON DELETE CASCADE,
  CONSTRAINT EMP_DOC_CHECKLISTS_DOC_FK     FOREIGN KEY (DOCUMENT_ID) REFERENCES EMPLOYEE_DOCUMENTS(DOCUMENT_ID)  ON DELETE SET NULL
);

