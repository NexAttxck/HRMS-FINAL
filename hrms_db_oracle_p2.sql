  CREATED_AT      NUMBER(12)   DEFAULT 0 NOT NULL,
  UPDATED_AT      NUMBER(12)   DEFAULT 0 NOT NULL,
  CONSTRAINT LMS_ENROLLMENTS_PK          PRIMARY KEY (ENROLLMENT_ID),
  CONSTRAINT LMS_ENROLLMENTS_UQ          UNIQUE      (MODULE_ID, EMPLOYEE_ID),
  CONSTRAINT LMS_ENROLLMENTS_STATUS_CHK  CHECK       (STATUS IN ('Not Started','In Progress','Completed')),
  CONSTRAINT LMS_ENROLLMENTS_MOD_FK      FOREIGN KEY (MODULE_ID)   REFERENCES LMS_MODULES(MODULE_ID) ON DELETE CASCADE,
  CONSTRAINT LMS_ENROLLMENTS_EMP_FK      FOREIGN KEY (EMPLOYEE_ID) REFERENCES EMPLOYEES(EMPLOYEE_ID) ON DELETE CASCADE
);

