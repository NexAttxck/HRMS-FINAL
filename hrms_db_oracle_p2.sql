CREATE TABLE PROJECT_TASKS (
  TASK_ID       NUMBER(10)    GENERATED ALWAYS AS IDENTITY,
  PROJECT_ID    NUMBER(10)    NOT NULL,
  EMPLOYEE_ID   NUMBER(10),
  TITLE         VARCHAR2(200) NOT NULL,
  PRIORITY      VARCHAR2(10)  DEFAULT 'Medium' NOT NULL,
  STATUS        VARCHAR2(15)  DEFAULT 'To Do'  NOT NULL,
  PROGRESS      NUMBER(3)     DEFAULT 0 NOT NULL,
  DUE_DATE      DATE,
  CREATED_AT    NUMBER(12)    DEFAULT 0 NOT NULL,
  CONSTRAINT PROJECT_TASKS_PK          PRIMARY KEY (TASK_ID),
  CONSTRAINT PROJECT_TASKS_PRIO_CHK    CHECK       (PRIORITY IN ('Low','Medium','High','Critical')),
  CONSTRAINT PROJECT_TASKS_STATUS_CHK  CHECK       (STATUS   IN ('To Do','In Progress','Completed','Cancelled')),
  CONSTRAINT PROJECT_TASKS_PROJ_FK     FOREIGN KEY (PROJECT_ID)  REFERENCES PROJECTS(PROJECT_ID)   ON DELETE CASCADE,
  CONSTRAINT PROJECT_TASKS_EMP_FK      FOREIGN KEY (EMPLOYEE_ID) REFERENCES EMPLOYEES(EMPLOYEE_ID) ON DELETE SET NULL
);

